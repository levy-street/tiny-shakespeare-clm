def _build_bias_vectors() -> dict[str, list[float]]:
    out: dict[str, list[float]] = {}
    lowers = "abcdefghijklmnopqrstuvwxyz"
    for prefix, entries in _S4.items():
        vec = [0.0] * VOCAB_SIZE
        # Mild negative for unlisted letters: penalize rare 4th-letter
        # continuations given a known 3-letter word start.
        neg = -2.0
        for target in lowers:
            if target not in entries:
                vec[VOCAB_INDEX[target]] = neg
        for nxt, bias in entries.items():
            if nxt in VOCAB_INDEX:
                vec[VOCAB_INDEX[nxt]] = bias
                if nxt.isalpha() and nxt.lower() == nxt:
                    up = nxt.upper()
                    if up in VOCAB_INDEX:
                        vec[VOCAB_INDEX[up]] = bias * 0.3
        out[prefix] = vec
    return out


START4GRAM_BIAS: dict[str, list[float]] = _build_bias_vectors()


_GLOBAL_SCALE = 1.3


def start4gram_bias(first_three: str) -> list[float] | None:
    """first_three is the lowercased first three letters of a new word.

    Returns a scaled bias vector or None if no entry.
    """
    if not first_three or len(first_three) != 3:
        return None
    key = first_three.lower()
    v = START4GRAM_BIAS.get(key)
    if v is None:
        return None
    return [x * _GLOBAL_SCALE for x in v]
