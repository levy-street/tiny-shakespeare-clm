def _build_bias_vectors() -> dict[str, list[float]]:
    out: dict[str, list[float]] = {}
    lowers = "abcdefghijklmnopqrstuvwxyz"
    for prefix, entries in _ST.items():
        vec = [0.0] * VOCAB_SIZE
        # Mild negative default for letters not listed — penalize rare
        # 3rd-letter continuations at word start.
        neg = -3.0
        for target in lowers:
            if target not in entries:
                vec[VOCAB_INDEX[target]] = neg
        for nxt, bias in entries.items():
            if nxt in VOCAB_INDEX:
                vec[VOCAB_INDEX[nxt]] = bias
                if nxt.isalpha() and nxt.lower() == nxt:
                    up = nxt.upper()
                    if up in VOCAB_INDEX:
                        vec[VOCAB_INDEX[up]] = bias * 0.3
        out[prefix] = vec
    return out


STARTTRIGRAM_BIAS: dict[str, list[float]] = _build_bias_vectors()


_GLOBAL_SCALE = 0.7


def starttrigram_bias(first_two: str) -> list[float] | None:
    """first_two is the lowercased first two letters of a new word.

    Returns a scaled bias vector or None if no entry.
    """
    if not first_two or len(first_two) != 2:
        return None
    key = first_two.lower()
    v = STARTTRIGRAM_BIAS.get(key)
    if v is None:
        return None
    return [x * _GLOBAL_SCALE for x in v]
